    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("/admin_pages/admin_send_message.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String roll_number = request.getParameter("rollnumber");
        String subject = request.getParameter("subject");
        String message = request.getParameter("message");

        UserDAO udao = new UserDAO();
        User checkUser = udao.getUserByRollnumber(roll_number);

        if (checkUser == null) {
            // redirect to login page with error message
            response.sendRedirect("/admin/send_message?error=rollnumber");
            return;
        }

        MessageDAO md = new MessageDAO();
        md.addNew(new Message(1, roll_number, subject, message, 1, new Date(System.currentTimeMillis()), -1));
        response.sendRedirect("/admin/send_message?message=send_success");
    }

